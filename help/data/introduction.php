      <div id="title" class="th">Introduction</div>
      <div class="content-body evenbg">
        <div class="topic justify" style="padding-bottom: 20px;">
          <p>
            Tab Mix Plus is a very popular extension for the Mozilla Firefox®
            browser that enhances Firefox's® tab browsing abilities. It includes
            such features as duplicating tabs, controlling tab focus, tab
            clicking options, undo closed tabs and windows, plus much more. It
            also includes a Session Manager with crash recovery that can save
            and restore combinations of opened tabs and windows.
          </p>

          <p>
            Tab Mix Plus come with Options window that includes all of its
            preferences as well as adding user interface to important Firefox
            hidden preferences. It is recommended that you make all change to
            the preference in the options window. The options window is available
            from the Add-ons Manager or form the tools menu (unless you turn this
            option off).
          </p>

          <p>
            All preferences that are set by Tab Mix Plus are stored in the
            prefs.js file in the
            <a href="https://support.mozilla.org/en-US/kb/profiles-where-firefox-stores-user-data">
            users profile</a> directory.&nbsp; These preferences
            can also be accessed and changed by entering "about:config" in the
            URL bar and "extensions.tabmix" in the filter box to narrow down the
            results from the search.
          </p>
          <ul>
            <li>Enter "about:config" in the URL box and press &lt;Enter&gt;</li>
            <li>Enter "extensions.tabmix" in the filter box to limit the results
            to Tab Mix Plus entries</li>
          </ul>
        </div>

