        <u><b>Protected tab</b></u>
        <p>A tab that is prevented from closing. You can load other sites in the tab and browse to other URLs in the tab. Notice that the close button is missing from the right side of the tab, so you cannot close the tab. If you try to close Firefox®, you will get a warning message about closing the protected tab.</p>

        <u><b>Locked tab</b></u>
        <p>A tab that is prevented from navigating to another page or URL. Your tab is locked on the current page or URL. Nothing new can load in the tab, but you can refresh the same page. All links will open in a new tab (link, history, bookmark, etc.). Notice that the close button is available, so you can close a locked tab.</p>

        <u><b>Frozen tab</b></u>
        <p>A tab that is both locked and protected. It will not close and nothing new can be loaded in it. You will not be able to navigate away from the current page. All links will open in a new tab (link, history, bookmark, etc.) Notice that the close button is missing from the right side of the tab, so you cannot close the tab. If you try to close Firefox®, you will get a warning message about closing the protected tab.</p>

