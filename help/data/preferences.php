          <img src="help/images/TMPSettingsRestoreImportExport.jpg" alt="image">
          <p>
          Since all the Tab Mix Plus preferences are stored in the 'prefs.js' file, Tab Mix Plus has
          the ability to extract these preferences and store them in a separate
          file. the 'Preferences' button is located on the bottom of the options
          window.<br>Click on it to open a popup menu that will dispaly to you
          the following options will be available to choose from a popup menu:
          </p>
          <ul>
            <li>Restore Defaults - will reset all preferences to the Tab Mix Plus default preferences</li>
          </ul>
          <ul>
            <li>Import Preferences - you may import Tab Mix Plus preferences from a file
              (usually from another configuration or PC)</li>
          </ul>
          <ul>
            <li>Export Preferences - you may export your preferences to be saved or
              applied to another PC, the default file name is TMPpref.txt. </li>
          </ul>
          <ul>
            <li>Sync Preferences - Enable to add Tabmix preferences to the data Firefox sync. For more help about sync, read the article
              <a href="https://support.mozilla.org/en-US/kb/how-do-i-set-up-firefox-sync?esab=a&s=Sync+settings&r=1&as=s">How do I set up Firefox Sync</a>
              from Mozilla support.
            </li>
          </ul>
