      <div id="title" class="th">How to post about a problem</div>
      <div class="content-body">
        <div class="topic">
          <p>
          Most issues reported in Tab Mix Plus forum usually end up to be extensions or theme conflicts.
          </p>
          <p>
          When posting, please, always include your OS (Operating System),
          <a href="https://support.mozilla.org/en-US/products/firefox">Firefox</a>
          and Tab Mix Plus versions. A list of your extensions could also be useful as somebody
          might spot a troublesome extension which can lead to a faster resolution.<br/>
          You can set your OS, Firefox and Tab Mix Plus information in the signature field in
          your profile (link to the User Control Panel is at the top of every page of
          the Forum). This way, you won't have to type it every time. Just remember to
          update it with Firefox and/or Tab Mix Plus update! <br/>
          It is very helpful if you can post the steps to reproduce your problem and
          if you experience an issue with a specific page, include a link(-s).
          </p><br/>

          <u>Save Troubleshooting Information from Firefox</u>
          <p>
          Use Firefox
          <a href="https://support.mozilla.org/en-US/kb/use-troubleshooting-information-page-fix-firefox?esab=a&s=about%3Asupport&r=0&as=s#w_accessing-the-troubleshooting-information-page">
          Troubleshooting Information page</a> to help us fix issues with Tab Mix Plus.
          Use <b>Copy text to clipboard</b> button on the Troubleshooting page and save the
          data to a text file. There are also a few extensions to help you with posting such
          info in forums, like
          <a href="https://addons.mozilla.org/en-US/firefox/addon/infolister/?src=search">
          Infolister</a> and
          <a href="https://addons.mozilla.org/en-US/firefox/addon/extension-list-dumper/?src=search">
          Extensions List Dumper</a>.
          </p><br/>

          <u>Save Tab Mix Plus Preferences</u><br/>
          You may be asked to post your Tab Mix Plus preferences. To do that, follow these steps:
          <ol>
            <li>Open Tab Mix Plus Options and click the 'Preferences' button at the bottom left corner.</li>
            <li>Choose 'Export Preferences' and save the file to a location on your hard drive.</li>
          </ol><br/>

          <u>Save Browser Console log</u>
          <p>
          The Browser Console is a tool provided by Firefox to log errors, warnings
          and messages for all content tabs, for add-ons, and for the browser's own code.
          </p>

          <p>
          The content of the Browser Console can provide important information about
          your problem. To get this information, open the Browser Console (Tools > Web
          Developer > Browser console) when you encounter the problem you want to
          report about, right click anywhere in the console and click select all. Copy
          everything and paste it to a text file.
          </p>

          <p>
          If your problem happens when Firefox starts, open the Browser Console
          immediately after Firefox starts and copy/paste its content to a text file,
          then clear the content and open new window and copy/paste the Browser Console
          content to another file.
          </p><br/>

          <u>Add attachment to your post</u><br/>
          Follow these steps to attach a file or a screenshot to your post to the Forum:
          <ol>
            <li>In your thread, click Full Editor button under the quick reply box.</li>
            <li>Locate Filename and Browse buttons, click and navigate to the file you just saved.</li>
            <li>Click Add File and Submit. Your file will be attached to your post.</li>
          </ol><br/>

          <u>Where to post</u><br/>
          To help the developers and users to track issues and get better search results, please remember to:
          <ul>
            <li>post problems with beta builds in the
            <a href="http://tmp.garyr.net/forum/viewforum.php?f=3">Builds forum</a>.
            </li>
            <li>Problems with display should be posted in the
            <a href="http://tmp.garyr.net/forum/viewtopic.php?f=2&t=13266">Display Issues sticky</a>.
            </li>
            <li>Problems with official releases should be posted in the
            <a href="http://tmp.garyr.net/forum/viewforum.php?f=2">Problems & Questions forum</a>.
            </li>
          </ul><br/>

          Thank you for your cooperation and Good Luck!
